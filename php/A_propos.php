    <p><strong>2. Quelles sont les principales différences entre les niveaux de politesse en français et en kirundi? </strong></p><br>

    <ol><b>Les niveaux de politesse en français et en kirundi présentent quelques différences. Voici un aperçu des principales distinctions :</b><br>
<b><u>En français :</u></b>

<li>Tutoiement et vouvoiement : En français, il existe une distinction formelle entre le "tu" et le "vous". Le "tu" est utilisé pour s'adresser à une personne familière ou dans des situations informelles, tandis que le "vous" est utilisé pour s'adresser à une personne de manière plus formelle ou dans des situations professionnelles.</li><br>
    <li>Titres et formules de politesse : En français, il est courant d'utiliser des titres et des formules de politesse pour montrer du respect dans les interactions formelles. Par exemple, "Monsieur", "Madame" ou "Mademoiselle" sont utilisés pour s'adresser aux personnes de manière respectueuse.</li><br>
   
<b><u>En kirundi :</u></b>
<li>Pronoms de politesse : En kirundi, il existe des pronoms spécifiques pour exprimer la politesse. Par exemple, <strong>"wewe" (tu) </strong> est utilisé pour s'adresser à une personne familière, tandis que <strong> <i>"mukuru" ou "umunyamuhande" </i>(vous) </strong> est utilisé pour s'adresser à une personne de manière plus formelle ou respectueuse.</ul><br>
<li>Formules de politesse : En kirundi, il existe des expressions spécifiques pour montrer du respect et de la politesse. Par exemple, <strong><i>"Amahoro"</i>(paix)</strong> est souvent utilisé comme salutation pour exprimer des souhaits de bien-être et de respect.</li><br>
